package org.string;

public class Numbers {
	public void decrement() {
		for (int k = 5; k < 1; k++) {
			
		
		for (int i = 1; i <= k; i++) {
			if (i<6) {
				System.out.println(i);
				
			}
			if (i<5) {
				System.out.println(i);
				
			}
		}
			
			
		}
				
	
		
	}
		
		
	
	public static void main(String[] args) {
		Numbers n = new Numbers();
		n.decrement();
	}
}
